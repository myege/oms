 package com.what21.model;
 
 
 public class CountRule
 {
   private Integer id;
   private String name;
   private String code;
   private String zgf;
   private String qgf;
   private String czf;
   private String ptf;
   private String tallage;
   
   public Integer getId() {
     return this.id;
   }
   public void setId(Integer id) {
     this.id = id;
   }
   public String getName() {
     return this.name;
   }
   public void setName(String name) {
     this.name = name;
   }
   public String getCode() {
     return this.code;
   }
   public void setCode(String code) {
     this.code = code;
   }
   public String getZgf() {
     return this.zgf;
   }
   public void setZgf(String zgf) {
     this.zgf = zgf;
   }
   public String getQgf() {
     return this.qgf;
   }
   public void setQgf(String qgf) {
     this.qgf = qgf;
   }
   public String getCzf() {
     return this.czf;
   }
   public void setCzf(String czf) {
     this.czf = czf;
   }
   public String getPtf() {
     return this.ptf;
   }
   public void setPtf(String ptf) {
     this.ptf = ptf;
   }
   public String getTallage() {
     return this.tallage;
   }
   public void setTallage(String tallage) {
     this.tallage = tallage;
   }
 }


