 package com.what21.model;
 public class Bond
 {
   private int id;
   private String bondMoney;
   private String companybm;
   private String companyName;
   private int userId;
   private Double res;
   private Double se;
   
   public Double getRes() {
     return this.res;
   }
   
   public void setRes(Double res) {
     this.res = res;
   }
   
   public Double getSe() {
     return this.se;
   }
   
   public void setSe(Double se) {
     this.se = se;
   }
   
   public int getId() {
     return this.id;
   }
   
   public void setId(int id) {
     this.id = id;
   }
   
   public String getBondMoney() {
     return this.bondMoney;
   }
   
   public void setBondMoney(String bondMoney) {
     this.bondMoney = bondMoney;
   }
   
   public String getCompanybm() {
     return this.companybm;
   }
   
   public void setCompanybm(String companybm) {
     this.companybm = companybm;
   }
   
   public String getCompanyName() {
     return this.companyName;
   }
   
   public void setCompanyName(String companyName) {
     this.companyName = companyName;
   }
   
   public int getUserId() {
     return this.userId;
   }
   
   public void setUserId(int userId) {
     this.userId = userId;
   }
 }


